package Activity1;
public class declarePrintVariables {
    public static void main(String[] args){
        System.out.println("Declaring and Printing Variables");
        int number = 10;
        char letter = 'a';
        boolean result = true;
        String str = "hello";
        
        System.out.println("Number = "+number);
        System.out.println("Letter = "+letter);
        System.out.println("Result = "+result);
        System.out.println("Str = "+str);
    }
}
